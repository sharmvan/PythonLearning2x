# range is actually a function which will give you multiple values.
for y in range(5):
    print(y)

# Assign this range to variable x
x = range(5)
print(x)  # This will tell you I am range from 0 to 5

# We can convert this range into list if we want to
x = list(range(5))
print(x)

# Is range keyword or not? It's not a keyword.
range = 190
print(range)

# Alone range is not a keyword but range with () is a function.
# range vs range()
