# Python Arithmetic Operators:
# Addition: +
# Subtraction: -
# Multiplication: *
# Division: /
# Modulo (Remainder): %
# Exponentation: **
x = 13
y = 10
z = x + y
print(z)
z = x - y
print(z)
z = x * y
print(z)
z = x / y
print(z)
z = x % y
print(z)
z = x ** y
print(z)
z = z + 5  # we can write like this also--> z += 5
print(z)
print(-z)  # To negate the value
# or
a = -z
print(-a)
