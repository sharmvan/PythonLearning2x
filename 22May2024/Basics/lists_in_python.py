# List can hold multiple item.
# Value of lists can be accessed through indexes.
# Duplicate values are allowed in the list.
x = [10, 40.2, "Vandana", "56", True, 100, 10]
print(x)
print(type(x))
print(x[0])
print(x[5])
print(x[3])
# print(x[8])  # IndexError: list index out of range
print(x[0:4])
print(x[1:5])
print(x[-1])
print(x[0:6:2])