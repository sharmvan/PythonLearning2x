# Que1: Explain the difference between the = operator and the == operator in Python.
# Ans: = is used for when we provide value to some variable. == is a comparison operator to compare values.
# for eg:
a = 10
print(a)
b = 20
print(a == b)

# Que2: What does the ** operator do in Python, and how is it used?
# Ans: it's an Arithmetic operator which is used for power.
# For eg:
x = 10
b = 2
print(b ** x)

